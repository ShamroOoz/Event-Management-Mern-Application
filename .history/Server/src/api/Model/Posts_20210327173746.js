import { Schema, model } from "mongoose";

const postSchema = Schema({
  title: String,
  message: String,
  creator: String,
  tags: [String],
  selectedFile: String,
  likeCount: {
    type: Number,
    default: 0,
  },
  createdAt: {
    type: Date,
    default: new Date(),
  },
});

const PostsModal = model("Posts", postSchema);
export default PostsModal;
